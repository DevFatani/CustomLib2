public class Tools {
    public static func calcSum (_ num1:Int, _ num2: Int) -> Int {
        num1 + num2
    }
    
    public static func calcSub (_ num1:Int, _ num2: Int) -> Int {
        num1 - num2
    }
    
    public static func calcMult (_ num1:Int, _ num2: Int) -> Int {
        num1 * num2
    }
    
    public static func calcDiv (_ num1:Int, _ num2: Int) -> Int {
        num1 / num2
    }
}
